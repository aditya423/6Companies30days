#include<bits/stdc++.h>
using namespace std;

int trailingZeroes(int n) {
    
    int count = 0;
    while(n){
        n /= 5;
        count += n;
    }

    return count;
}

int main() {

    cout<<trailingZeroes(3)<<endl;

    cout<<trailingZeroes(5)<<endl;

    return 0;
}

